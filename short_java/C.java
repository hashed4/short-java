package short_java;
import java.util.*;
import java.io.*;

public class C{
	public static void out(String data){
		System.out.print(data);
	}
	
	public static void out(boolean data){
		System.out.print(data);
	}	

	public static void out(int data){
		System.out.print(data);
	}
	public static void outln(String data){
		System.out.println(data);
	}
	public static void outln(boolean data){
		System.out.println(data);
	}
	public static void outln(int data){
		System.out.println(data);
	}

}